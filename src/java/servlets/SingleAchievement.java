                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/samaj", "root", "root");
                    
        PreparedStatement p = con.prepareStatement("Select * from achievementTable where name=?");
